#!/usr/bin/env python3
"""Concatenates two strings."""


def concat(str1: str, str2: str) -> str:
    """Args:
        str1 (str): The first string to concatenate.
        str2 (str): The second string to concatenate.
    Returns:
        str: The concatenated string composed of str1 followed by str2."""
    return str1 + str2
